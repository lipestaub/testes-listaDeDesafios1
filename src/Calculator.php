<?php
    class Calculator {
        /**
         * Adds an item to the items list if it does not already exist.
         *
         * @param int $a The first number of the sum.
         * @param int $b The second number of the sum;
         * @return int
         */
        public function sum(int $a, int $b): int {
            return $a + $b;
        }
    }
?>